Num = int(input("Dime un numero del 1 al 7"))
if Num == 1:
    print(f"Tu dia de la semana es: Lunes")
elif Num ==2:
    print(f" Es martes")
elif Num == 3:
    print(f"Es miercoles")
elif Num ==4:
    print(f" Es jueves")
elif Num == 5:
    print(f"Es viernes")
elif Num ==6:
    print(f" Es sabado")
elif Num == 7:
    print(f"Es domingo")
else:
    print("Error ingrese valor del 1 al 7.")